GAME_DESIGN_CONTEXT = """
# 多人对战（PvP/PvE）核心设计原则

## 1. 核心博弈维度

### 1.1 集火与保排 (Focus Fire vs Protect)
- 2v2中最常见的问题是"两人集火秒杀一人"
- 历代通过以下设计工具平衡：
  - **单体保护**：完全抵挡一次攻击，但有使用次数/频率限制
  - **嘲讽/吸引机制**：强制敌方以特定单位为目标，保护队友
  - **范围保护**：保护全体免受范围攻击，通常也有频率限制
  - **先手压制**：使对方在本回合无法正常行动

### 1.2 站场收益与联防轮转
- 站场（Stacking）：持续在场获得优势
- 联防（Pivot）：通过换人/退出避免被击杀
- 关键设计：
  - **攻击后换人**：攻击附带换人效果，实现进退自如
  - **降能力后换人**：攻击附带削弱效果，增加交换价值
  - **强制换人**：通过攻击或效果强制对方切换单位

### 1.3 速度线与行动顺序
- 速度属性决定出手顺序
- 双打中先手优势极大（可配合集火秒杀）
- 历代调整速度线的方式：
  - **空间类效果**：反转速度顺序，让慢速单位优先行动
  - **全员加速**：使己方全体提升行动频率
  - **全员减速**：使对方全体降低行动频率

### 1.4 AOE与单体伤害的权衡
- 范围攻击(AOE)在多人中效果极强
- 历代平衡方式：
  - 范围伤害威力折扣
  - 部分强力AOE设定为可被格挡的接触型
  - 通过环境/场地效果限制AOE效果

## 2. PvE多人共斗设计问题

### 2.1 状态失控 (Status Stacking)
- 问题：多人可叠加大量debuff，使Boss失去挑战性
- 解决方案演进：
  - Boss周期性清空自身负面状态
  - Boss对特定状态类型免疫
  - Boss反击机制（攻击施加debuff的玩家，惩罚堆叠）

### 2.2 等待时间 (Wait Time)
- 问题：多人轮流行动，等待时间过长
- 解决方案演进：
  - 半即时制时间轴
  - 协作指令（可选择让AI代理操作）
  - 快速战斗模式

### 2.3 贡献度不均 (Freeloading)
- 问题：部分玩家挂机蹭奖励
- 解决方案：
  - 战斗参与度影响奖励分配
  - Boss锁定攻击特定玩家，增加每个玩家的存在感

## 3. 机制迭代历史

### 爆发资源演进（通用框架）
- 目的：为对战提供"爆发资源"，增加战术选择
- 演进方向（通用）：
  - 使用门槛从高到低（特定单位专属 → 全员可用）
  - 持续时间从长到短
  - 博弈深度越来越高（需要更多决策）

### 特性/被动系统演进
- 隐藏特性/被动技能的引入增加了配队多样性
- 部分特性在多人中被禁用或调整

## 4. 竞技规则迭代

### 赛季规则体系
- 每个赛季通过规则表限制可用内容
- 逐步加大限制力度，防止环境固化
- 目的：防止Meta固化，保持环境新鲜

## 5. 设计意图分析框架

分析某个版本改动时，应该思考：
1. **解决了什么问题？** - 玩家反馈？数据失衡？
2. **对不同玩家群体的影响？** - 休闲/竞技/PvE
3. **与其他机制的交互？** - 是否会产生新的Combo？
4. **是否符合游戏整体方向？** - 简化？深化？
5. **对后续版本的影响？** - 是否成为固定机制？
"""

