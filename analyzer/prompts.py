GAME_DESIGN_CONTEXT = """
# 宝可梦多人对战（VGC/双打）设计原则

## 1. 核心博弈维度

### 1.1 集火与保排 (Focus Fire vs Protect)
- 2v2中最常见的问题是"两人集火秒杀一人"
- 历代通过以下机制平衡：
  - **守住(Protect)**：完全抵挡一次攻击，但无法连续使用
  - **看我嘛(Follow Me)**：吸引对方攻击，保护队友
  - **广域防守(Wide Guard)**：保护全体免受范围攻击
  - **击掌奇袭(Fake Out)**：先制攻击使对方退缩，无法行动

### 1.2 站场收益与联防轮转
- 站场（Stacking）：持续在场获得优势
- 联防（Pivot）：通过换人避免被击杀
- 关键技能：
  - **急速折返(U-turn)**：攻击后强制换人
  - **抛下狠话(Parting Shot)**：攻击+降对手能力后换人
  - **蜻蜓回转(Dragon Tail)**：强制换人+造成伤害

### 1.3 速度线与行动顺序
- 宝可梦的速度(Speed)决定出手顺序
- 双打中：先手优势极大（可配合集火秒杀）
- 历代调整速度线的方式：
  - 空间类技能（ Trick Room）：反转速度顺序
  - 顺风(Tailwind)：全体速度翻倍
  - 黏黏网(Sticky Web)：降低对方全体速度

### 1.4 AOE与单体伤害的权衡
- 范围攻击(AOE)在双打中效果极强
- 历代平衡方式：
  - 范围伤害威力降低25%（第二世代）
  - 部分强力AOE设定为接触技（可被防护）
  - 通过天气/场地限制AOE效果

## 2. PvE多人共斗设计问题

### 2.1 状态失控 (Status Stacking)
- 问题：4人可叠加大量debuff，使Boss成为木桩
- 解决方案演进：
  - **Boss清空自身负面状态**（如极巨团体战Boss的护盾机制）
  - **Boss对特定状态免疫**（免疫混乱、睡眠等）
  - **Boss反击机制**（攻击施加debuff的玩家）

### 2.2 等待时间 (Wait Time)
- 问题：4人轮流行动，等待时间过长
- 解决方案演进：
  - **半即时制时间轴**（朱紫太晶团体战）
  - **协作指令**（可选择让AI代理操作）
  - **快速战斗模式**

### 2.3 贡献度不均 (Freeloading)
- 问题：部分玩家挂机蹭奖励
- 解决方案：
  - 战斗参与度影响奖励
  - Boss锁定攻击特定玩家

## 3. 机制迭代历史

### Mega进化 (第六世代) → Z招式 (第七世代) → 极巨化 (第八世代) → 太晶化 (第九世代)
- 目的：为对战提供"爆发资源"，增加战术选择
- 演进方向：
  - 门槛越来越低（从需要特定宝可梦→任何宝可梦）
  - 持续时间越来越短（3回合→1回合）
  - 博弈深度越来越高

### 特性系统演进
- 隐藏特性（Hidden Ability）的引入增加了配队多样性
- 部分特性在多人中被禁用或调整

## 4. 竞技规则迭代

### VGC 规则赛季
- 每个赛季通过规则表(Ban List)限制可用宝可梦
- 规则D：禁止超过6只特定强力宝可梦
- 规则E：进一步限制
- 目的：防止Meta固化，保持环境新鲜

## 5. 设计意图分析框架

分析某个版本改动时，应该思考：
1. **解决了什么问题？** - 玩家反馈？数据失衡？
2. **对不同玩家群体的影响？** - 休闲/竞技/PvE
3. **与其他机制的交互？** - 是否会产生新的Combo？
4. **是否符合游戏整体方向？** - 简化？深化？
5. **对后续世代的影响？** - 是否成为固定机制？
"""

